import { readdirSync, readFileSync, statSync, writeFileSync } from 'node:fs'
import { dirname, join, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const rootDir = dirname(dirname(fileURLToPath(import.meta.url)))
const appDir = join(rootDir, 'apps/desktop')
const srcDir = join(appDir, 'src')
const remixiconCssPath = join(appDir, 'node_modules/remixicon/fonts/remixicon.css')
const outputPath = join(srcDir, 'remixicon-subset.css')

const sourceFilePattern = /\.(ts|tsx|css)$/
const iconPattern = /(?<![a-zA-Z0-9])ri-[a-z0-9-]+/g

const listSourceFiles = (dir) =>
  readdirSync(dir).flatMap((entry) => {
    const path = join(dir, entry)
    const stat = statSync(path)
    if (stat.isDirectory()) return listSourceFiles(path)
    if (sourceFilePattern.test(entry) && path !== outputPath) return [path]
    return []
  })

const remixiconCss = readFileSync(remixiconCssPath, 'utf8')
const iconRules = new Map(
  [...remixiconCss.matchAll(/^\.(ri-[a-z0-9-]+):before\s*\{\s*content:\s*"([^"]+)";\s*\}/gm)]
    .map((match) => [match[1], match[2]]),
)

const usedIcons = [
  ...new Set(
    listSourceFiles(srcDir).flatMap((path) =>
      [...readFileSync(path, 'utf8').matchAll(iconPattern)].map((match) => match[0]),
    ),
  ),
].sort()

const missingIcons = usedIcons.filter((icon) => !iconRules.has(icon))
if (missingIcons.length > 0) {
  console.error(`Missing Remixicon rules for: ${missingIcons.join(', ')}`)
  process.exit(1)
}

const fontVersion = remixiconCss.match(/remixicon\.woff2\?t=(\d+)/)?.[1] ?? Date.now()
const relativeFontPath = relative(srcDir, join(appDir, 'node_modules/remixicon/fonts/remixicon.woff2'))

const output = [
  '/* Generated by scripts/generate-remixicon-subset.mjs. Do not edit by hand. */',
  '@font-face {',
  '  font-family: "remixicon";',
  `  src: url("${relativeFontPath}?t=${fontVersion}") format("woff2");`,
  '  font-display: swap;',
  '}',
  '',
  '[class^="ri-"], [class*=" ri-"] {',
  "  font-family: 'remixicon' !important;",
  '  font-style: normal;',
  '  -webkit-font-smoothing: antialiased;',
  '  -moz-osx-font-smoothing: grayscale;',
  '}',
  '',
  ...usedIcons.map((icon) => `.${icon}:before { content: "${iconRules.get(icon)}"; }`),
  '',
].join('\n')

writeFileSync(outputPath, output)
console.log(`Wrote ${relative(rootDir, outputPath)} with ${usedIcons.length} Remixicon rules.`)
